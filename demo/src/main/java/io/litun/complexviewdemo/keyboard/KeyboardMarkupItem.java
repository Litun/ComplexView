public class KeyboardMarkupItem {
    private float x;
    private float y;
    private float width;
    private float height;
    private String letter;
    private String action;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }

    public String getLetter() {
        return letter;
    }

    public String getAction() {
        return action;
    }
}
